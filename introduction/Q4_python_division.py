# Python: Division

# Problem

# Given two integers `a` and `b`, print:
# - The result of integer division (`a // b`).
# - The result of float division (`a / b`).
# Each result should be printed on a new line.

## Approach
# - Read two integers from input.
# - Use `//` for integer division.
# - Use `/` for float division.
# - Print both results on separate lines.

## Code

a = int(input())
b = int(input())

print(a // b)
print(a / b)

## Complexity
# - Time Complexity: O(1)
# - Space Complexity: O(1)
